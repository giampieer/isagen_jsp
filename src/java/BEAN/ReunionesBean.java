
package BEAN;


public class ReunionesBean {
    private int NUMERO;
    private String PERSONAL;
    private String FECHA;
    private String ACUERDOS;
    private String REUNION;
    private String DURACION;
    private int NUMPROY;
 private String NOMBPROY;

    public int getNUMERO() {
        return NUMERO;
    }

    public void setNUMERO(int NUMERO) {
        this.NUMERO = NUMERO;
    }

    public String getPERSONAL() {
        return PERSONAL;
    }

    public void setPERSONAL(String PERSONAL) {
        this.PERSONAL = PERSONAL;
    }

    public String getFECHA() {
        return FECHA;
    }

    public void setFECHA(String FECHA) {
        this.FECHA = FECHA;
    }

    public String getACUERDOS() {
        return ACUERDOS;
    }

    public void setACUERDOS(String ACUERDOS) {
        this.ACUERDOS = ACUERDOS;
    }

    public String getREUNION() {
        return REUNION;
    }

    public void setREUNION(String REUNION) {
        this.REUNION = REUNION;
    }

    public String getDURACION() {
        return DURACION;
    }

    public void setDURACION(String DURACION) {
        this.DURACION = DURACION;
    }

    public int getNUMPROY() {
        return NUMPROY;
    }

    public void setNUMPROY(int NUMPROY) {
        this.NUMPROY = NUMPROY;
    }

    public String getNOMBPROY() {
        return NOMBPROY;
    }

    public void setNOMBPROY(String NOMBPROY) {
        this.NOMBPROY = NOMBPROY;
    }

    
}
