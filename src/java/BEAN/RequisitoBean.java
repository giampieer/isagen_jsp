
package BEAN;

public class RequisitoBean {
    private int numero;
    private String alcance;
    private int personal;
    private int reunion;
    private String descripcion;
    private int NUMPROY;
 private String NOMBPROY;

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getAlcance() {
        return alcance;
    }

    public void setAlcance(String alcance) {
        this.alcance = alcance;
    }

    public int getPersonal() {
        return personal;
    }

    public void setPersonal(int personal) {
        this.personal = personal;
    }

    public int getReunion() {
        return reunion;
    }

    public void setReunion(int reunion) {
        this.reunion = reunion;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getNUMPROY() {
        return NUMPROY;
    }

    public void setNUMPROY(int NUMPROY) {
        this.NUMPROY = NUMPROY;
    }

    public String getNOMBPROY() {
        return NOMBPROY;
    }

    public void setNOMBPROY(String NOMBPROY) {
        this.NOMBPROY = NOMBPROY;
    }
    
    
}
