
package BEAN;

public class ProblemaBean {
   private int numero;
   private String nivel;
   private String descripcion;
   private String perjudicado;
  private int NUMPROY;
 private String NOMBPROY;

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPerjudicado() {
        return perjudicado;
    }

    public void setPerjudicado(String perjudicado) {
        this.perjudicado = perjudicado;
    }

    public int getNUMPROY() {
        return NUMPROY;
    }

    public void setNUMPROY(int NUMPROY) {
        this.NUMPROY = NUMPROY;
    }

    public String getNOMBPROY() {
        return NOMBPROY;
    }

    public void setNOMBPROY(String NOMBPROY) {
        this.NOMBPROY = NOMBPROY;
    }

    
}
